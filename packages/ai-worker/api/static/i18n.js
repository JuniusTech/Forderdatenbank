let uiLang = localStorage.getItem("ui_lang") || "de";

function t(key, vars = {}) {
  let text = (I18N[uiLang] && I18N[uiLang][key]) || I18N.de[key] || key;
  Object.entries(vars).forEach(([k, v]) => {
    text = text.replace(`{${k}}`, String(v));
  });
  return text;
}

function setLang(lang) {
  if (!I18N[lang]) return;
  uiLang = lang;
  localStorage.setItem("ui_lang", lang);
  document.documentElement.lang = lang === "de" ? "de" : lang;
  applyI18n();
  if (typeof onLangChange === "function") onLangChange();
}

function applyI18n() {
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    const attr = el.getAttribute("data-i18n-attr");
    const text = t(key);
    if (attr) el.setAttribute(attr, text);
    else el.textContent = text;
  });
  document.querySelectorAll("#lang-select option").forEach((opt) => {
    opt.selected = opt.value === uiLang;
  });
}
